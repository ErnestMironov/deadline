import { createApp } from 'vue'
import { createPinia } from 'pinia'
import router from './router'
import './style.css'
import App from './App.vue'

const app = createApp(App)
const pinia = createPinia()

app.use(pinia)
app.use(router)

try {
  app.mount('#app')
} catch (error) {
  console.error('Failed to mount app:', error)
}
